/* <sc-view> DEMO_DB.INTERVAL_DATA </sc-view> */
CREATE OR REPLACE VIEW DEMO_DB.DEMO_DB.INTERVAL_DATA
AS
SELECT
DATEADD('YEAR', CAST(INTERVAL_YEAR_TYPE AS INTEGER),
TO_DATE('2000-01-01')) INTERVAL_YEAR,
DATEADD('MONTH', CAST(INTERVAL_MONTH_TYPE AS INTEGER),
TO_DATE('2000-01-01')) INTERVAL_MONTH,
DATEADD('MONTH', DEMO_DB.PUBLIC.INTERVAL2MONTHS_UDF(INTERVAL_YEAR2MONTH_TYPE),
TO_DATE('2000-01-01')) INTERVAL_YR2MON,
DATEADD('DAY', CAST(INTERVAL_DAY_TYPE AS INTEGER),
TIMESTAMP '2000-01-01 01:01:01.500-01:00') INTERVAL_DAY,
DATEADD('SECONDS', DEMO_DB.PUBLIC.INTERVAL2SECONDS_UDF('DAY TO HOUR', INTERVAL_DAY2HOUR_TYPE),
TIMESTAMP '2000-01-01 01:01:01.500-01:00') INTERVAL_DAY2HR,
DATEADD('SECONDS', DEMO_DB.PUBLIC.INTERVAL2SECONDS_UDF('DAY TO MINUTE', INTERVAL_DAY2MINUTE_TYPE),
TIMESTAMP '2000-01-01 01:01:01.500-01:00') INTERVAL_DAY2MIN,
DATEADD('SECONDS', DEMO_DB.PUBLIC.INTERVAL2SECONDS_UDF('DAY TO SECOND', INTERVAL_DAY2SECOND_TYPE),
TIMESTAMP '2000-01-01 01:01:01.500-01:00') INTERVAL_DAY2SEC,
DATEADD('HOUR', CAST(INTERVAL_HOUR_TYPE AS INTEGER),
TIMESTAMP '2000-01-01 01:01:01.500-01:00') INTERVAL_HR,
DATEADD('SECONDS', DEMO_DB.PUBLIC.INTERVAL2SECONDS_UDF('HOUR TO MINUTE', INTERVAL_HOUR2MINUTE_TYPE),
TIMESTAMP '2000-01-01 01:01:01.500-01:00') INTERVAL_HR2MIN,
DATEADD('SECONDS', DEMO_DB.PUBLIC.INTERVAL2SECONDS_UDF('HOUR TO SECOND', INTERVAL_HOUR2SECOND_TYPE),
TIMESTAMP '2000-01-01 01:01:01.500-01:00') INTERVAL_HR2SEC,
DATEADD('MINUTE', CAST(INTERVAL_MINUTE_TYPE AS INTEGER),
TIMESTAMP '2000-01-01 01:01:01.500-01:00') INTERVAL_MIN,
DATEADD('SECONDS', DEMO_DB.PUBLIC.INTERVAL2SECONDS_UDF('MINUTE TO SECOND', INTERVAL_MINUTE2SECOND_TYPE),
TIMESTAMP '2000-01-01 01:01:01.500-01:00') INTERVAL_MIN2SEC,
DATEADD('SECOND', CAST(INTERVAL_SECOND_TYPE AS DECIMAL(8, 6)),
TIMESTAMP '2000-01-01 01:01:01.500-01:00') INTERVAL_SEC
FROM DEMO_DB.DEMO_DB.INTERVAL_DATA_TYPE_V;